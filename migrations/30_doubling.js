const TokenSale = artifacts.require('TokenSale')
const moment = require('moment')
const { forEachSeries } = require('p-iteration')

async function performMigration(deployer, network, accounts) {
  const DeployedTokenSale = await TokenSale.deployed()
  const DTXBatches = []
  let DTXHoldersWei = {}
  let counter = 1

  const lockup = moment('2018-10-01 00:00:00+01:00').unix()

  Object.keys(tokenHolders).forEach(address => {
    if (counter % 50 === 0) {
      DTXBatches.push(DTXHoldersWei)
      DTXHoldersWei = {}
    }
    DTXHoldersWei[address] = web3.utils.toWei(tokenHolders[address].toString())
    counter++
    if (Object.keys(tokenHolders).length === counter) {
      DTXBatches.push(DTXHoldersWei)
    }
  })
  await forEachSeries(DTXBatches, async batch => {
    await DeployedTokenSale.handleExternalBuyers(
      Object.keys(batch),
      new Array(Object.values(batch).length).fill(0),
      Object.values(batch),
      new Array(Object.values(batch).length).fill(lockup)
    )
  })
}

module.exports = function(deployer, network, accounts) {
  deployer
    .then(function() {
      return performMigration(deployer, network, accounts)
    })
    .catch(error => {
      console.log(error)
      process.exit(1)
    })
}

